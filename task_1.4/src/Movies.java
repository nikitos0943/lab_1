public class Movies {
    String name;
    int year;
    String country;
    String genre;
    int rentalCost;

    public  Movies(){

    }

    public Movies(String name, int year, String country, String genre, int rentalCost){
        this.name = name;
        this.year = year;
        this.country = country;
        this.genre = genre;
        this.rentalCost = rentalCost;
    }
}
